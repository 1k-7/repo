        sts_msg = await message.reply("‚è≥ …™…¥…™·¥õ…™·¥Ä·¥õ…™…¥…¢ ·¥õ·¥è·¥ò-·¥Ö·¥è·¥°…¥ ·¥Ö·¥Ä·¥õ·¥Ä  ô·¥Ä ü·¥Ä…¥·¥Ñ·¥á...\n·¥õ ú…™Íú± ·¥°…™ ü ü ·¥õ·¥Ä·¥ã·¥á ·¥Ä  ü·¥è…¥…¢ ·¥õ…™·¥ç·¥á.\n\nÍú∞…™…¥·¥Ö…™…¥…¢ ·¥Ä·¥Ñ·¥õ…™·¥†·¥á ·¥Ö·¥Ä·¥õ·¥Ä ô·¥ÄÍú±·¥á...")
